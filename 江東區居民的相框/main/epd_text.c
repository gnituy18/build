void epd_show_message(const char *const *lines, int n_lines, uint8_t fg, uint8_t bg)
{
    uint8_t *buf = malloc(EPD_BUFFER_SIZE);
    if (!buf) {
        ESP_LOGE(TAG, "no heap for message buffer");
        return;
    }
    memset(buf, (bg << 4) | bg, EPD_BUFFER_SIZE);

    /* Largest scale (capped at 6) that keeps the widest line on screen */
    int scale = 6;
    for (int i = 0; i < n_lines; i++) {
        int len = (int)strlen(lines[i]);
        while (scale > 1 && len * 8 * scale > EPD_WIDTH - 16) scale--;
    }

    int line_h = 8 * scale + scale * 2;
    int top = (EPD_HEIGHT - n_lines * line_h) / 2;
    if (top < 0) top = 0;

    for (int i = 0; i < n_lines; i++) {
        int text_w = (int)strlen(lines[i]) * 8 * scale;
        int x = (EPD_WIDTH - text_w) / 2;
        if (x < 0) x = 0;
        draw_string(buf, x, top + i * line_h, lines[i], scale, fg);
    }

    epd_init();
    epd_display(buf, EPD_BUFFER_SIZE);
    epd_sleep();
    free(buf);
}

static void draw_string(uint8_t *buf, int x, int y, const char *s, int scale, uint8_t fg)
{
    for (int cx = x; *s; s++, cx += 8 * scale) {
        draw_char(buf, cx, y, *s, scale, fg);
    }
}

