/* Each font pixel expands to scale×scale panel pixels */
static void draw_char(uint8_t *buf, int x, int y, char c, int scale, uint8_t fg)
{
    if (c < 0x20 || c > 0x7E) c = '?';
    const unsigned char *glyph = FONT8X8[(int)c - 0x20];
    for (int row = 0; row < 8; row++) {
        unsigned char bits = glyph[row];
        for (int col = 0; col < 8; col++) {
            if (bits & (1u << col)) {
                for (int dy = 0; dy < scale; dy++)
                    for (int dx = 0; dx < scale; dx++)
                        put_px(buf, x + col * scale + dx, y + row * scale + dy, fg);
            }
        }
    }
}

/* Pack one 4-bit color (2 px/byte, high nibble = left pixel); clips silently */
static void put_px(uint8_t *buf, int x, int y, uint8_t color)
{
    if (x < 0 || x >= EPD_WIDTH || y < 0 || y >= EPD_HEIGHT) return;
    int idx = (y * EPD_WIDTH + x) / 2;
    if (x & 1) {
        buf[idx] = (buf[idx] & 0xF0) | (color & 0x0F);
    } else {
        buf[idx] = (buf[idx] & 0x0F) | ((color & 0x0F) << 4);
    }
}
